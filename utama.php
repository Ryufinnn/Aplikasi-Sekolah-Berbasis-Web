      <br><strong>KABUPATEN PIDIE </strong></p>
      <p align="justify" style="line-height: 2em; text-indent:0.50in;">Sebelah barat berbatasan dengan kebupaten Aceh Besar, sebelah utara   berbatasan dengan selat malaka dan sebelah selatan berbatasan dengan   Kabupaten Aceh Barat Secara administratif, Kabupaten Pidie terdiri dari 30 Kecamatan, 128 Kemukiman, 29 Kelurahan, dan 923 Desa, namun pada tanggal <em>15 Juni tahun 2007</em> melalui undang-undang Nomor 7 Tahun 2007 tentang pembentukan Kabupaten   Pidie Jaya, Kabupaten Pidie mengalami pemekaran menjadi 2 (dua)   Kabupaten yaitu Pidie sebagai Kabupaten Induk dan Pidie Jaya sebagai   Kabupaten pemekaran, sehingga saat ini wilayah Kabupaten Pidie terdiri   atas 23 Kecamatan, 94 Kemukiman, 732 Gampong (yang sebelumnya 15 gampong   di Kecamatan Kota Sigli, 4 di Grong-grong dan 1 di Kec.Mutiara yang   sesuai dengan Qanun menjadi Kelurahan ,.</p>
      <div id="post-body-947078555251470569" itemprop="articleBody">
      <p align="justify" style="line-height: 2em; text-indent:0.50in;">Sedangkan   wilayah yang termasuk dalam Kabupaten Pidie Jaya merupakan wilayah   bekas Kewedanaan Meureudu yang melingkupi 8 (delapan) Kecamatan (Bandar   Baru, Pante Raja, Trenggadeng, Meureudu, Meurah Dua, Jangka Buya, Bandar   Dua dan Ulim).</p>
      <p align="justify" style="line-height: 2em; text-indent:0.50in;">Pembentukan   Kabupaten Pidie Jaya diyakini akan memberikan manfaat bagi pembangunan   nasional pada umumnya dan pembangunan daerah pada khususnya,   meningkatkan kesejahteraan masyarakat dan sekaligus dapat memberikan   dampak positif yaitu meningkatkan pendapatan daerah. Melalui pemekaran   tersebut diharapkan akan semakin efektifnya pelaksanaan fungsi-fungsi   utama pemerintah Kabupaten, baik Kabupaten Induk maupun Kabupaten   pemekaran di bidang perlindungan, pemberdayaan dan pelayanan masyarakat   dengan berbagai produk regulasi kebijakan yang akan menyertainya.</p>
      <p align="justify" style="line-height: 2em; text-indent:0.50in;">Pada   sisi lain, bentuk-bentuk pelayanan yang diaktualisasikan oleh   pemerintah Kabupaten tersebut akan semakin terarah dan berorientasi pada   kebutuhan riel masyarakat setempat termasuk dalam hal mengoptimalkan   pemanfaatan potensi unggulan yang memiliki baik oleh Kabupaten Induk   maupun Kabupaten pemekaran. Adapun rincian wilayahnya adalah sebagai   berikut:</p>
      <p align="justify" style="line-height: 2em; text-indent:0.50in;">Mengenyam pendidikan pada institusi pendidikan formal yang diakui oleh   lembaga pendidikan negara adalah sesuatu yang wajib dilakukan di   Indonesia.  Mulai dari anak tukang sapu jalan, anak tukang dagang   martabak mesir, anak tukang jambret, anak pak tani, anak bisnismen, anak   pejabat tinggi negara, dan sebagainya harus bersekolah minimal selama 9   tahun lamanya hingga lulus SMP.<br />
        <br />
Mungkin banyak dari kita yang mempertanyakan apakah sebenarnya fungsi   pendidikan formal tersebut.  Situs organisasi.org ini akan membantu   memberikan sedikit jawaban sesuai dengan kondisi yang ada.  Kenapa kita   harus sekolah dan mengapa semakin tinggi jenjang pendidikan kita maka   semakin baik?<br />
<br />
<strong>Manfaat dan Fungsi Belajar di Sekolah dan di Perguruan Tinggi :</strong><br />
<br />
1. Melatih Kemampuan Kemampuan Akademis Anak (Biar Pintar)<br />
<br />
Dengan melatih serta mengasah kemampuan menghafal, menganalisa,   memecahkan masalah, logika, dan lain sebagainya maka diharapkan   seseorang akan memiliki kemampuan akademis yang baik.  Orang yang tidak   sekolah biasanya tidak memiliki kemampuan akademis yang baik sehingga   dapat dibedakan dengan orang yang bersekolah.  Kehidupan yang ada di   masa depan tidaklah semudah dan seindah saat ini karena dibutuhkan   perjuangan dan kerja keras serta banyak ilmu pengetahuan.<br />
<br />
2. Menggembleng dan Memperkuat Mental, Fisik dan Disiplin<br />
<br />
Dengan mengharuskan seorang siswa atau mahasiswa datang dan pulang   sesuai dengan aturan yang berlaku maka secara tidak langsung dapat   meningkatkan kedisiplinan seseorang.  Dengan begitu padatnya jadwal   sekolah yang memaksa seorang siswa untuk belajar secara terus-menerus   akan menguatkan mental dan fisik seseorang menjadi lebih baik.<br />
<br />
3. Memperkenalkan Tanggung Jawab<br />
<br />
Tanggung jawab seorang anak adalah belajar di mana orangtua atau wali   yang memberi nafkah.  Seorang anak yang menjalankan tugas dan   kewajibannya dengan baik dengan bersekolah yang rajin akan membuat   bangga orang tua, guru, saudara, famili, dan lain-lain.<br />
<br />
4. Membangun Jiwa Sosial dan Jaringan Pertemanan<br />
<br />
Banyaknya teman yang bersekolah bersama akan memperluas hubungan sosial   seorang siswa.  Tidak menutup kemungkinan di masa depan akan membentuk   jaringan bisnis dengan sesama teman di mana di antara sesamanya sudah   saling kenal dan percaya.  Dengan memiliki teman maka kebutuhan sosial   yang merupakan kebutuhan dasar manusia dapat terpenuhi dengan baik.<br />
<br />
5. Sebagai Identitas Diri<br />
<br />
Lulus dari sebuah institusi pendidikan biasanya akan menerima suatu   sertifikat atau ijazah khusus yang mengakui bahwa kita adalah orang yang   terpelajar, memiliki kualitas yang baik dan dapat diandalkan.  Jika   disandingkan dengan orang yang tidak berpendidikan dalam suatu lowongan   pekerjaan kantor, maka rata-rata yang terpelajarlah yang akam   mendapatkan pekerjaan tersebut.<br />
<br />
6. Sarana Mengembangkan Diri dan Berkreativitas<br />
<br />
Seorang siswa dapat mengikuti berbagai program ekstrakurikuler sebagai   pelengkap kegiatan akademis belajar mengajar agar dapat mengembangkan   bakat dan minat dalam diri seseorang.  Semakin banyak memiliki keahlian   dan daya kreativitas maka akan semakin baik pula kualitas seseorang.    Sekolah dan kuliah hanyalah sebagai suatu mediator atau perangkat   pengembangan diri.  Yang mengubah diri seseorang adalah hanyalah orang   itu sendiri.<br />
<br />
Memang proses belajar manusia sangat lama dan panjang.  Bayangkan saja   jika sekolah dasar (SD) memakan waktu 6 tahun, sekolah menengah pertama   dan sekolah menengah atas selama 6 tahun, di perguruan tinggi selama 4   tahun makan waktu yang diperlukan untuk meraih gelar sarjana yaitu   sekitar kurang lebih 16 tahun</p>
